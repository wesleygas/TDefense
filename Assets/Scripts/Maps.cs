    public static bool IsGround(int x, int y){
        int j = x + xSize/2 + 3;
        int i = ySize/2 - y - 1;
        //Debug.Log($"Position: ({x},{y}) - Map[{i},{j}] = {maps[currentMap,i,j]} ");
